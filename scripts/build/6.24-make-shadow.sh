#!/bin/bash
set -e

# 6.24. Shadow-4.8.1
# The Shadow package contains programs for handling passwords in a secure way.

echo "Building Shadow..."
echo "Approximate build time: 0.2 SBU"
echo "Required disk space: 46 MB"

tar -xf /sources/shadow-*.tar.* -C /tmp/ \
  && mv /tmp/shadow-* /tmp/shadow \
  && pushd /tmp/shadow

# Disable the installation of the groups program and its man pages, as Coreutils
# provides a better version. Also, prevent the installation of manual pages that
# were already installed:
sed -i 's/groups$(EXEEXT) //' src/Makefile.in
find man -name Makefile.in -exec sed -i 's/groups\.1 / /'   {} \;
find man -name Makefile.in -exec sed -i 's/getspnam\.3 / /' {} \;
find man -name Makefile.in -exec sed -i 's/passwd\.5 / /'   {} \;

# nstead of using the default crypt method, use the more secure SHA-512 method
# of password encryption, which also allows passwords longer than 8 characters.
# It is also necessary to change the obsolete /var/spool/mail location for user
# mailboxes that Shadow uses by default to the /var/mail location used
# currently:
sed -i -e 's@#ENCRYPT_METHOD DES@ENCRYPT_METHOD SHA512@' \
       -e 's@/var/spool/mail@/var/mail@' etc/login.defs

# Make a minor change to make the first group number generated by useradd 1000:
sed -i 's/1000/999/' etc/useradd

# Prepare Shadow for compilation:
./configure --sysconfdir=/etc --with-group-name-max-length=32

# Compile the package:
make

# Install the package:
make install

# 6.24.2. Configuring Shadow
# Enable shadowed passwords:
pwconv

# Enable shadowed group passwords:
grpconv

# 6.24.3. Setting the root password
# Set a password for user root:
echo "root:root" | chpasswd

popd \
  && rm -rf /tmp/shadow
